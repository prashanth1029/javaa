//implicit type casting occurs automatically when you asign a lower type variable to the upper type variable
public class ImplicitCast {

	public static void main(String[] args) {

		byte i=45;
		int c=i;
		System.out.println(c);
		char r='a';
		int x=r;
		System.out.println(x);
		long l=i;
		System.out.println(i);
	}

}

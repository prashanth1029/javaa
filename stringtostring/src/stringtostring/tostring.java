package stringtostring;

public class tostring {
	int i;
	int t;
	int y;
	int w;
	int r;
	int n;
	int j;

	public tostring(int i, int j, int t, int y, int w, int r, int n) {
		this.i = i;
		this.j = j;
		this.t = t;
		this.y = y;
		this.w = w;
		this.r = r;
		this.n = n;

	}

public String toString(){
return "["+i+" "+j+" "+t+" "+y+" "+w+" "+r+" "+n+"]";
}
}
